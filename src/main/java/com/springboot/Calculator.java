package com.springboot;

public class Calculator {
    public int Add(int a, int b)
    {
        return a+b;
    }

    public  int multiple(int a, int b)
    {
        return a*b;
    }
    public  int devide(int a, int b)
    {
        return a/b;
    }
    public  int subtract(int a, int b)
    {
        return a-b;
    }
}
